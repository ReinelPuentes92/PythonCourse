#Clase: Define atributos comportamientos que los objetos tendran.
#Objeto: Es una instancia de una clase, es una entidad concreta que tiene estado (atributos), y comportamientos
# (metodos) que contiene una clase. 
#Metodo: Es una funcion asociada a una clase que define el comportamiento de los objetos de esa clase.

#Ejemplo
palabra = 'Reinel Fernando Puentes Moreno.'
# Para colocar las palabras en minuscula
print(palabra.lower())
# Para colocar solo la primera palabra en mayuscula
print(palabra.capitalize())
# Para colocar toda la frase en mayuscula
print(palabra.upper())
# Coloca todas las primeras palabras en mayuscula
print(palabra.title())
# Remplaza ciertos caracteres por alguna condicion
print(palabra.replace('u','n'))
