# Ciclo iterante for:
# range: estipula intervalo, estructura range(inicio, fin, salto)

print(range(5))

# Recorrido en valores negativos, esto es para recorrer de manera decreciente
for i in range(10,1,-1):
    print(i)

# Recorrido del iterante en una cadena de strings
for i in 'Reinel':
    print('i es:', i)

# Recorrido del iterante en una seire de nuemros
for i in range(0,11,1):
    if (i % 2) == 0:
         print(i)

